package code.java;

class ClaseA {
    void forClasico1() {
        for(int i=0; i<10; i++) {
            System.out.println(" i es " + i);
        }
    }

    void forClasico2() {
        for(int i=10; i>0; i--) {
            System.out.println(" i es " + i);
        }
    }

    void forClasico3() {
        for(int i=10; i>=0; i--) {
            System.out.println(" i es " + i);
        }
    }

    void forClasico4() {
        for(int i=0; i<=10; i++) {
            System.out.println(" i es " + i);
        }
    }
}
