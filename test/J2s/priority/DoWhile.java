package code.java;

class DoWhileDemo {
    public static void main(String[] args){
        int count = 1;
        do {
            System.out.println("Count is: " + count);
            count++;
        } while (count < 11);


        int x = 10;
        do {
            System.out.print("value of x : " + x );
            x++;
            System.out.print("\n");
        } while( x < 20 );

    }
}
